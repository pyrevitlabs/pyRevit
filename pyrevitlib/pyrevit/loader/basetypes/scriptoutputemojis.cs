        public static string Emojize(string textWithEmojiShorthand) {
            var matches = new Regex(@"\:(?<code>[^\s]+?)\:").Matches(textWithEmojiShorthand);
            foreach (Match match in matches) {
                var emojiShortCode = match.Groups["code"].Value;
                if (emojiDict.ContainsKey(emojiShortCode)) {
                    var shortHand = string.Format(":{0}:", emojiShortCode);
                    var emojiFile = string.Format("{0}.png", emojiDict[emojiShortCode]);
                    var emojiPath = Path.Combine(Path.GetDirectoryName(typeof(CommonUtils).Assembly.Location), "emojis", emojiFile);
                    textWithEmojiShorthand = textWithEmojiShorthand.Replace(shortHand, string.Format(emojiImageTag, emojiPath, shortHand));
                }
            }

            return textWithEmojiShorthand;
        }
